'use strict';
/**===========================================================================**
    Require
**===========================================================================**/
//Gulp
var gulp = require('gulp');
//Wrench
var wrench = require('wrench');

/**===========================================================================**
    Import Gulp Folder
**===========================================================================**/
wrench.readdirSyncRecursive('./gulp').filter(function(file) {
    return (/\.(js|coffee)$/i).test(file);
}).map(function(file) {
    require('./gulp/' + file);
});

/**===========================================================================**
    Task
**===========================================================================**/
gulp.task('default', ['serve']);
